X	"HAL: A multi-paradigm approach to automatic data path synthesis"
X	in Proc. 23rd IEEE Design Automation Conf, Las Vegas, NV, pp. 263-270,
X	July 1986.
X
Xgoing by the code supplied in Brewer and Gajski, the algorithm looks like:
X
X	y = f(x, u); (x0, y0); x0 <= x < a; u = dy/dx
X
X	while (x < a) {
X		u = u - dx . ( u . FIVE . x + THREE . y );
X		x = x + dx;
X		y = y + u . dx;
X	}
X
X	where FIVE and THREE are some programmable register values.
X	FIVE = 5; THREE = 3;
X
XOperation:
X	
X	load initial values for x, y, u, dx, and a into their
X	respective registers and let it roll; new values for x, y
X	and u appear at the ports at periodic interval until x < a.
X
X*	Rajesh Gupta 
X*	July 10, 1990
X*
X*/
X
X#include "../templates/library.hc"
X#define SIZE	12
X
Xprocess diffeq(xport, yport, uport, dxport, aport, reset, ready, next, over)
X
X	in port aport[SIZE];
X	in port dxport[SIZE];
X	in port reset;
X	in port ready;		/* next data point available at the inputs */
X	out port next;		/* next (x, y) ready */
X	out port over;		/* current loop over */
X	inout port xport[SIZE], yport[SIZE], uport[SIZE]; 
X	
X{
X	register three[SIZE];
X	register five[SIZE];
X	register a[SIZE], x[SIZE], y[SIZE], dx[SIZE], u[SIZE];
X
X        static u1[SIZE], u2[SIZE], u3[SIZE], u4[SIZE], u5[SIZE], u6[SIZE],
X		y1[SIZE];
X	if (reset) {
X		x = 0;
X		y = 0;
X		u = 0;
X		dx = 0;
X		a = 0;
X		load three = 3;
X		load five = 5;
X	} else [
X
X		repeat{}
X		until(ready);
X
X		/* load initial values */
X		<
X		x = read(xport);
X		y = read(yport);
X		u = read(uport);
X		dx = read(dxport);
X		a = read(aport); 
X		>;
X
X		repeat {
X				u1 = u * dx;
X				u2 = five * x;
X				u3 = three * y;
X				y1 = u * dx;
X				x = x + dx;
X				u4 = u1 * u2;
X				u5 = dx * u3;
X				y = y + y1;
X				u6 = u - u4;
X				u = u6 - u5; 
X				< write xport = x;
X				write yport = y;
X				write uport = u; 
X				write next=1;>
X				write next=0;
X		} until (x >= a);
X		write over=1;
X		write over=0;
X	]
X}
