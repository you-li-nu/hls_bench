X *
X diffeq: hardware description for a small fixed-point 
X calculation loop.
X
X this example is taken from:
X
XF. Brewer, D.  Gajski: 
X	"Chippe: A System for Constraint Driven Behavioral Synthesis"
X	in IEEE Trans CAD vol 9, no 7, July 1990
