#ifndef SC_MULTI_CLOCK_TOP_H
#define SC_MULTI_CLOCK_TOP_H

#include"systemc.h"
#include"dut1.h"
#include"dut2.h"

SC_MODULE(sc_multi_clock)
{
	//Ports
	sc_in <bool>  clock1;
	sc_in <bool>  clock2;
	sc_in <bool>  reset; 
	sc_in <bool>  start; 
	sc_out<bool>  done1;
	sc_out<bool>  done2;

    sc_fifo_out<int> dout;
    sc_fifo_in<int> din;
	
	//Variables
    sc_fifo<int> chn;
    dut1 U_dut1;
    dut2 U_dut2;

	//Constructor
	SC_CTOR(sc_multi_clock)
	:clock1("clock1")
	,clock2("clock2")
	,reset("reset")
	,start("start")
	,done1("done1")
	,done2("done2")
	,U_dut1("U_dut1")
	,U_dut2("U_dut2")
    ,chn("U_chn", 4)
	{
       U_dut1.clock(clock1);
       U_dut1.reset(reset);
       U_dut1.start(start);
       U_dut1.done(done1);
       U_dut1.dout(chn);
       U_dut1.din(din);
		
       U_dut2.clock(clock2);
       U_dut2.reset(reset);
       U_dut2.start(start);
       U_dut2.done(done2);
       U_dut2.dout(dout);
       U_dut2.din(chn);
	}
};

#endif

